/**
 * check-translation-lag.mjs
 *
 * Compares en docs against a committed source-hash baseline for zh
 * translations. This avoids timestamp-based checks, which are noisy under
 * rebases, squashes, restored worktrees, and local uncommitted edits.
 *
 * Usage:
 *   node ./scripts/check-translation-lag.mjs
 *   node ./scripts/check-translation-lag.mjs --strict
 *   node ./scripts/check-translation-lag.mjs --update-key reference-api-core
 *   node ./scripts/check-translation-lag.mjs --bootstrap-baseline
 *
 * Output:
 *   src/generated/translation-lag.ts  — map of stale translationKey values
 *   src/content/docs/translation-source-hashes.json — committed baseline
 */

import crypto from 'node:crypto';
import { readdirSync, statSync, readFileSync } from 'node:fs';
import fs from 'node:fs/promises';
import path from 'node:path';
import { writeGeneratedFile, WEBSITE_ROOT } from './_shared.mjs';

const DOCS_EN = path.join(WEBSITE_ROOT, 'src', 'content', 'docs', 'docs');
const DOCS_ZH = path.join(WEBSITE_ROOT, 'src', 'content', 'docs', 'zh', 'docs');
const BASELINE_PATH = path.join(WEBSITE_ROOT, 'src', 'content', 'docs', 'translation-source-hashes.json');

function normalizeSource(content) {
  return content
    .replace(/^---\r?\n[\s\S]*?\r?\n---\r?\n?/, '')
    .replace(/\r\n/g, '\n')
    .trim();
}

function sourceHash(filePath) {
  const content = normalizeSource(readFileSync(filePath, 'utf8'));
  return crypto.createHash('sha256').update(content).digest('hex');
}

function extractTranslationKey(filePath) {
  const content = readFileSync(filePath, 'utf8');
  const m = content.match(/^translationKey:\s*(.+)$/m);
  return m ? m[1].trim() : null;
}

function walkMdx(dir, lang, keyMap) {
  function recurse(current) {
    for (const entry of readdirSync(current)) {
      const full = path.join(current, entry);
      if (statSync(full).isDirectory()) {
        recurse(full);
      } else if (entry.endsWith('.mdx')) {
        const key = extractTranslationKey(full);
        if (key) {
          const existing = keyMap.get(key) ?? {};
          if (lang === 'en') {
            existing.enPath = full;
          } else {
            existing.zhPath = full;
          }
          keyMap.set(key, existing);
        }
      }
    }
  }
  recurse(dir);
}

async function readBaseline() {
  try {
    return JSON.parse(await fs.readFile(BASELINE_PATH, 'utf8'));
  } catch (err) {
    if (err?.code === 'ENOENT') return {};
    throw err;
  }
}

async function writeBaseline(baseline) {
  const sorted = Object.fromEntries(Object.entries(baseline).sort(([a], [b]) => a.localeCompare(b)));
  await fs.writeFile(BASELINE_PATH, `${JSON.stringify(sorted, null, 2)}\n`, 'utf8');
}

function staleTranslationsModule(staleMap) {
  return [
    '// This file is generated by website/scripts. Do not edit by hand.',
    `export const STALE_TRANSLATIONS = ${JSON.stringify(staleMap, null, 2)} as const;`,
    'export const TRANSLATION_LAG = STALE_TRANSLATIONS;',
    '',
  ].join('\n');
}

function buildKeyMap() {
  const keyMap = new Map();
  walkMdx(DOCS_EN, 'en', keyMap);
  walkMdx(DOCS_ZH, 'zh', keyMap);
  return keyMap;
}

async function updateBaselineForKeys(keyMap, keys) {
  const baseline = await readBaseline();
  for (const key of keys) {
    const entry = keyMap.get(key);
    if (!entry?.enPath || !entry?.zhPath) {
      throw new Error(`cannot update translation baseline for missing key: ${key}`);
    }
    baseline[key] = sourceHash(entry.enPath);
  }
  await writeBaseline(baseline);
}

export async function checkTranslationLag(options = {}) {
  const keyMap = buildKeyMap();

  if (options.bootstrapBaseline) {
    const keys = [...keyMap.entries()]
      .filter(([, { enPath, zhPath }]) => enPath && zhPath)
      .map(([key]) => key);
    await updateBaselineForKeys(keyMap, keys);
  }

  if (options.updateKeys?.length) {
    await updateBaselineForKeys(keyMap, options.updateKeys);
  }

  const baseline = await readBaseline();
  const staleMap = {};

  for (const [key, { enPath, zhPath }] of keyMap.entries()) {
    if (!enPath || !zhPath) continue;

    const currentHash = sourceHash(enPath);
    if (baseline[key] !== currentHash) {
      staleMap[key] = true;
    }
  }

  await writeGeneratedFile('translation-lag.ts', staleTranslationsModule(staleMap));

  const flagged = Object.keys(staleMap).length;
  if (flagged > 0) {
    console.warn(`[check-translation-lag] ${flagged} zh page(s) need source-hash baseline updates.`);
    for (const key of Object.keys(staleMap)) {
      console.warn(`  ${key}: english source content differs from recorded translation baseline`);
    }
  } else {
    console.log('[check-translation-lag] All zh pages match their recorded source baselines.');
  }

  return flagged;
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const strict = process.argv.includes('--strict');
  const bootstrapBaseline = process.argv.includes('--bootstrap-baseline');
  const updateKeys = process.argv
    .map((arg, index, args) => (arg === '--update-key' ? args[index + 1] : null))
    .filter(Boolean);

  const flagged = await checkTranslationLag({ bootstrapBaseline, updateKeys });
  if (strict && flagged > 0) {
    process.exit(1);
  }
}
